    func testExample() {
        func defaultSwitch(number: Int) -> String {
            switch number {
            case 1: return "One"
            case 2: return "Two"
            case 3: return "Three"
            case 4: return "Four"
            case 5: return "Five"
            case 6: return "Six"
            case 7: return "Seven"
            case 8: return "Eight"
            case 9: return "Nine"
            default: return "Zero"
            }
        }
        
        let switchObj = SwitchItUp()
        for _ in 0..<100 {
            let value = Int.random(in: 0 ..< 10) % 10 //from 0 to 9
            XCTAssertEqual(switchObj.switchItUp(value), defaultSwitch(number: value))
        }
    }

